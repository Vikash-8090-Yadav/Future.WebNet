  function sort() {
    var value = document.getElementById("chooseItem").value;
  
    if (value === "hlt") {
      WomensData.sort(function (a, b) {
        return Number(b.price) - Number(a.price);
      });
    }
    if (value === "lth") {
      WomensData.sort(function (a, b) {
        return Number(a.price) - Number(b.price);
      });
    }
    if (value === "discount") {
      WomensData.sort(function (a, b) {
        a = Math.round(((a.strikedoffprice - a.price) / a.strikedoffprice) * 100);
        b = Math.round(((b.strikedoffprice - b.price) / b.strikedoffprice) * 100);
        return b - a;
      });
    }
    if (value === "new") {
      WomensData.sort(function (a, b) {
        return a.new - b.new;
      });
    }
    if (value === "popular") {
      WomensData.sort(function (a, b) {
        return b.popular - a.popular;
      });
    }
    if (value === "rating") {
      WomensData.sort(function (a, b) {
        return b.rating - a.rating;
      });
    }
    if (value === "Recommended") {
      return 1;
    }
    displayData(WomensData);
    console.log(WomensData);
  }
  





